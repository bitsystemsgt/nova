    private void llenajComboPerfiles() throws SQLException {
        
        DefaultComboBoxModel jcp = new DefaultComboBoxModel();
        jComboPerfiles.setModel(jcp);
//consulta los perfiles disponibles en la base de datos        
        ResultSet rs = modeloPerfiles.mConsultaPerfiles("");
                
        while(rs.next()){
//envia los datos de la consulta al metodo perfil
            jcp.addElement(new perfil(rs.getString(2), rs.getString(1)));
        }
        modeloPerfiles.cierraSesion();
        
    }
    public class perfil {
//almacena los perfiles del jComboPerfiles y envia el id cuando se llama
          private String nombre;
          private String id ;
      
          public perfil(String nombre , String id ) {
                this.nombre=nombre;
                this.id=id;
          }
     
            public String getID(){
                return id ;
            }
     
            public String toString() {
                return nombre ;
            }
    }
